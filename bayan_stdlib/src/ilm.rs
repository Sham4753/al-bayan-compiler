//! الجذر: ع-ل-م (البحث والاستعلام)

/// استعلام بسيط (عَلِمَ)
pub fn علم_بـ(استعلام: &str, مصدر: &[&str]) -> bool {
    مصدر.contains(&استعلام)
}

/// استعلام متقدم (اِستَعلَمَ)
pub fn استعلم(استعلام: &str, بيانات: &[(String, String)]) -> Option<String> {
    for (مفتاح, قيمة) in بيانات {
        if مفتاح == استعلام {
            return Some(قيمة.clone());
        }
    }
    None
}

/// تدريب نموذج (عَلَّمَ)
pub fn علِّم(بيانات: &[f64], تصنيفات: &[f64]) -> String {
    format!("🧠 تم تدريب النموذج على {} عينة", بيانات.len().min(تصنيفات.len()))
}

/// حالة الكائن (اِعتَلَمَ)
pub fn حالة(كائن: &str) -> String {
    format!("📊 حالة '{}': نشط", كائن)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_علم_بـ() {
        let مصدر = vec!["أحمد", "محمد", "علي"];
        assert!(علم_بـ("محمد", &مصدر));
        assert!(!علم_بـ("خالد", &مصدر));
    }

    #[test]
    fn test_استعلم() {
        let بيانات = vec![
            ("اسم".to_string(), "أحمد".to_string()),
            ("عمر".to_string(), "30".to_string()),
        ];
        assert_eq!(استعلم("اسم", &بيانات), Some("أحمد".to_string()));
        assert_eq!(استعلم("بلد", &بيانات), None);
    }
}
